const formatReportDate = (date) => {
  if (!date) return "-";

  const d = new Date(date);
  if (Number.isNaN(d.getTime())) return "-";

  return d.toLocaleDateString("en-GB", {
    day: "2-digit",
    month: "short",
    year: "numeric",
  });
};

const formatReportCurrency = (amount) => {
  return `Rs ${Number(amount || 0).toLocaleString("en-PK")}`;
};



const REPORT_FOOTER_TEXT = "Powered by Cybrox - cybrox.info";

const addReportFooter = (doc) => {
  const pages = doc.bufferedPageRange();

  for (let i = pages.start; i < pages.start + pages.count; i += 1) {
    doc.switchToPage(i);

    const footerY = doc.page.height - 24;

    doc
      .font("Helvetica")
      .fontSize(8)
      .fillColor("#6b7280")
      .text(REPORT_FOOTER_TEXT, 0, footerY, {
        width: doc.page.width,
        align: "center",
        lineBreak: false,
      });

    doc.fillColor("#111827");
  }
};

const getMonthName = (month) => {
  if (!month) return "-";

  return new Date(2026, Number(month) - 1, 1).toLocaleString("en-US", {
    month: "long",
  });
};

const getSavedReportPeriodText = (report) => {
  if (report.report_filter_type === "date_range") {
    return `${formatReportDate(report.from_date)} to ${formatReportDate(report.to_date)}`;
  }

  return `${getMonthName(report.report_month)} ${report.report_year}`;
};

const createSavedReportPdf = (report, res, preview = false) => {
  const isWelfare = report.portal_type === "welfare";

  const doc = new PDFDocument({
    size: "A4",
    margin: 35,
    bufferPages: true,
  });

  res.setHeader("Content-Type", "application/pdf");
  res.setHeader(
    "Content-Disposition",
    `${preview ? "inline" : "attachment"}; filename="${report.report_no}.pdf"`
  );

  doc.pipe(res);

  const pageWidth = doc.page.width;
  const logoPath = path.join(__dirname, "../../assets/logo.png");

  if (fs.existsSync(logoPath)) {
    doc.image(logoPath, pageWidth / 2 - 38, 25, {
      width: 76,
      height: 76,
    });
  }

  doc.y = 115;

  doc
    .font("Helvetica-Bold")
    .fontSize(16)
    .fillColor("#111827")
    .text(isWelfare ? "Welfare Monthly Report" : "Vocational Monthly Report", {
      align: "center",
    });

  doc.moveDown(0.4);

  doc
    .font("Helvetica")
    .fontSize(10)
    .text(`Report Generated: ${formatReportDate(report.generated_at)}`, {
      align: "center",
    });

  doc.moveDown(1);

  doc
    .font("Helvetica-Bold")
    .fontSize(10)
    .text("Report Details:");

  const detailText = [
    `Report No: ${report.report_no}`,
    `Report Period: ${getSavedReportPeriodText(report)}`,
    `Generated By: ${report.generated_by_name || "-"}`,
  ];

  if (!isWelfare) {
    detailText.splice(1, 0, `Branch Name: ${report.branch_name || "-"}`);
  }

  doc.font("Helvetica").fontSize(9);

  detailText.forEach((text) => {
    doc.text(text);
  });

  doc.moveDown(1);

  const rows = isWelfare
    ? [
      ["Previous Balance", formatReportCurrency(report.previous_balance)],
      ["Donations", formatReportCurrency(report.donations)],
      ["Charity", formatReportCurrency(report.charity_amount)],
      ["Expenses", formatReportCurrency(report.expenses)],
      ["Current Balance", formatReportCurrency(report.current_balance)],
    ]
    : [
      ["Previous Balance", formatReportCurrency(report.previous_balance)],
      ["Collected Fees", formatReportCurrency(report.collected_fees)],
      ["Expenses", formatReportCurrency(report.expenses)],
      ["Current Balance", formatReportCurrency(report.current_balance)],
    ];

  const startX = 70;
  let y = doc.y;

  doc.rect(startX, y, 455, 26).fill("#eff6ff");

  doc
    .fillColor("#111827")
    .font("Helvetica-Bold")
    .fontSize(9)
    .text("Particular", startX + 8, y + 9, {
      width: 240,
    })
    .text("Amount", startX + 280, y + 9, {
      width: 160,
      align: "right",
    });

  y += 26;

  rows.forEach((row, index) => {
    if (index % 2 === 0) {
      doc.rect(startX, y, 455, 30).fill("#f8fafc");
    }

    doc
      .fillColor("#111827")
      .font(index === rows.length - 1 ? "Helvetica-Bold" : "Helvetica")
      .fontSize(9)
      .text(row[0], startX + 8, y + 10, {
        width: 240,
      })
      .text(row[1], startX + 280, y + 10, {
        width: 160,
        align: "right",
      });

    y += 30;
  });

  addReportFooter(doc);
  doc.end();
};

